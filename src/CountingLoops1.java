class CountingLoops {
    public static void main(String[] args) {
        // Task 1: Count up by 1 from 0 to 30
        System.out.print("Task 1: ");
        for (int i = 0; i <= 30; i++) {
            System.out.print(i + " ");
        }
        System.out.println(); // Adds a line break after the loop
    }
}